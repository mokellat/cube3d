#include "libft.h"

void	ft_putendl_fd(char *s, int g_fd)
{
	int i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			write(g_fd, &s[i], 1);
			i++;
		}
		write(g_fd, "\n", 1);
	}
}
